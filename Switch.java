import java.util.*;


public class Switch {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        String fruit = sc.next();

        switch(fruit){
            case "orange":
                System.out.println("SWEETTTT");
                break;
            case "apple":
                System.out.println("SWEETTTT");
                break;
            case "melon":
                System.out.println("SWEETTTT");
                break;
            case "berry":
                System.out.println("SWEETTTT");
                break;
            case "guava":
                System.out.println("SWEETTTT");
                break;
            default:
                System.out.println("Invalid Input");
        }
    }
}
